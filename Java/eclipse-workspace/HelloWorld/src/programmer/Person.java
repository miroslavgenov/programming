package programmer;

public class Person {
	private String name;
	private Integer age;
	
	public Person(String name,Integer age) {
		setName(name);
		setAge(age);
	}
	
	public void setName(String value) {
		this.name=value;
	}
	public void setAge(Integer value) {
		this.age=value;
	}
	
	
}




















































